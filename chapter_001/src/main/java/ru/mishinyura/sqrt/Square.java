package ru.mishinyura.sqrt;

/**
 * Класс Square реализует вычисление квадрата числа.
 *
 * @author Mishin Yura (mishin.inbox@gmail.com)
 * @since 04.06.2018
 */
public class Square {
    /**
     * Метод возводит число в квадрат.
     *
     * @param number Число.
     * @return Результат.
     */
    public final int sqr(final int number) {
        return number * number;
    }
}
